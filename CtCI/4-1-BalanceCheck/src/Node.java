
public class Node {
	Node right;
	Node left;
	
	public Node() {
		right = null;
		left = null;
	}
	
	public Node getRight() {
		return right;
	}
	
	public Node getLeft() {
		return left;
	}
	
	public void setRight(Node right) {
		this.right = right;
	}
	
	public void setLeft(Node left) {
		this.left = left;
	}
}
